package com.example.day03;

public class IfExam1 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        if(a < b) {
            System.out.println("a가 b보다 작습니다.");
        } else { System.out.println("a가 b보다 큽니다."); }

        if (a % 2 == 0) {
            System.out.println("a는 짝수 입니다.");
        } else { System.out.println("a는 홀수 입니다."); }
    }
}
